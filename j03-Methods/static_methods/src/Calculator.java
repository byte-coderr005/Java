public class Calculator {
    //Static method
    public static int topla(int a, int b) {
        return a + b;
    }
    public int carp(int a, int b) {
        return a * b;
    }
    public static int bol(int a, int b) {
        return a / b;
    }
    public int cikar(int a, int b) {
        return a - b;
    }
}
